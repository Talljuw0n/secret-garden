// Open Leader Modal
function openLeaderModal(leaderId) {
    const leader = leadersData[leaderId];
    if (!leader) return;
    
    const modal = document.getElementById('leaderModal');
    document.getElementById('modalLeaderImage').src = leader.image;
    document.getElementById('modalLeaderName').textContent = leader.name;
    document.getElementById('modalLeaderTitle').textContent = leader.title;
    document.getElementById('modalLeaderBio').innerHTML = `<p>${leader.bio}</p>`;
    
    modal.classList.add('active');
    document.body.style.overflow = 'hidden'; // Prevent background scrolling
}

// Close Leader Modal
function closeLeaderModal() {
    const modal = document.getElementById('leaderModal');
    modal.classList.remove('active');
    document.body.style.overflow = ''; // Restore scrolling
}

// Close modal when clicking outside
document.addEventListener('click', function(event) {
    const modal = document.getElementById('leaderModal');
    if (event.target === modal) {
        closeLeaderModal();
    }
});

// Close modal with Escape key
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closeLeaderModal();
    }
});
